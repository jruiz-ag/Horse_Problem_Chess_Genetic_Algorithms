#include "libft.h"

double	ft_sqrt(int num)
{
	double	n;

	n = 0;
	while ((n * n) <= num)
		n++;
	n -= 1;
	while ((n * n) <= num)
		n += 0.01;
	n -= 0.01;
	while ((n * n) <= num)
		n = n + 0.001;
	n -= 0.001;
	return (n);
}
